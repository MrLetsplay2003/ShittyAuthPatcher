package me.mrletsplay.shittyauthpatcher.version.meta;

public class MetadataLoadException extends RuntimeException {

	private static final long serialVersionUID = 2663401452073120976L;

	public MetadataLoadException() {
		super();
	}

	public MetadataLoadException(String message, Throwable cause) {
		super(message, cause);
	}

	public MetadataLoadException(String message) {
		super(message);
	}

	public MetadataLoadException(Throwable cause) {
		super(cause);
	}
	
}
